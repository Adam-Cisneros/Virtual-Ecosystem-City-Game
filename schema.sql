create table
  public.entities (
    id integer generated by default as identity not null,
    nourishment integer null,
    biome_id integer not null,
    entity_type text null,
    constraint entity_pkey primary key (id),
    constraint entitys_biome_id_fkey foreign key (biome_id) references biomes (id) on delete cascade
  ) tablespace pg_default;

create table
  public.jobs (
    id integer generated by default as identity not null,
    job_name text not null,
    building_id integer not null,
    constraint jobs_pkey primary key (id),
    constraint jobs_job_name_key unique (job_name),
    constraint jobs_building_id_fkey foreign key (building_id) references buildings (id) on delete cascade
  ) tablespace pg_default;

