create table
  public.biomes (
    id integer generated by default as identity not null,
    biome_name text null,
    constraint biome_pkey primary key (id)
  ) tablespace pg_default;

create table
  public.buildings (
    id integer generated by default as identity not null,
    name text null,
    quantity integer null,
    constraint inventory_pkey primary key (id)
  ) tablespace pg_default;

